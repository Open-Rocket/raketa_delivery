#!/bin/bash

#############################
# 1. ОСНОВНЫЕ КОМАНДЫ
#############################

# Показать текущую директорию
pwd

# Перейти в указанную директорию
cd /path/to/directory

# Вернуться в предыдущую директорию
cd -

# Вернуться в домашнюю директорию
cd ~

# Создать новую папку
mkdir my_folder

# Удалить пустую папку
rmdir my_folder

# Удалить файл
rm file.txt

# Удалить папку и всё её содержимое
rm -r my_folder

# Переместить или переименовать файл
mv old_name.txt new_name.txt
mv old_name.txt /new/path/

# Копировать файл
cp file.txt /new/path/

# Копировать папку
cp my_folder /backup/

# Создать пустой файл
touch new_file.txt

# Показать содержимое файла
cat file.txt

# Показать список файлов в директории
ls

# Показать список файлов с подробностями
ls -l

# Показать скрытые файлы
ls -a

#############################
# 2. РАБОТА С ПРОЦЕССАМИ
#############################

# Показать список запущенных процессов
ps aux

# Показать активные процессы в реальном времени
top

# Завершить процесс по PID
kill -9 PID

# Завершить все процессы с определённым именем
pkill process_name

# Показать фоновые процессы
jobs

# Вернуть фоновый процесс на передний план
fg %1

#############################
# 3. УПРАВЛЕНИЕ ПОЛЬЗОВАТЕЛЯМИ И ПРАВАМИ
#############################

# Узнать текущего пользователя
whoami

# Список пользователей в системе
who

# Выполнить команду от имени администратора
sudo command

# Изменить права доступа к файлу
chmod 755 script.sh

# Сменить владельца файла
sudo chown username file.txt

# Изменить пароль пользователя
passwd

#############################
# 4. РАБОТА С СЕТЬЮ
#############################

# Проверить соединение с сервером
ping google.com

# Скачать файл по URL
wget https://example.com/file.zip

# Отправить HTTP-запрос
curl https://example.com

# Показать сетевые интерфейсы
ifconfig  # Linux/macOS
ip a       # Альтернативный способ в Linux

#############################
# 5. УСТАНОВКА ПАКЕТОВ
#############################

# Ubuntu/Debian (apt)
sudo apt update
sudo apt install package
sudo apt remove package

# macOS (Homebrew)
brew install package
brew uninstall package

# Python (pip)
pip install package
pip uninstall package

# Node.js (npm)
npm install package
npm uninstall package

# Docker
docker pull image
docker run image
docker ps

#############################
# 6. ОБОЛОЧКА (SHELL)
#############################

# Вывести текст в консоль
echo "Hello, World!"

# Выполнить файл как скрипт
source script.sh

# Определить переменную окружения
export PATH="$HOME/.local/bin:$PATH"

#############################
# 7. АРХИВЫ И СЖАТИЕ
#############################

# Создать tar-архив
tar -cvf archive.tar folder/

# Распаковать tar-архив
tar -xvf archive.tar

# Создать zip-архив
zip -r archive.zip folder/

# Распаковать zip-архив
unzip archive.zip

#############################
# 8. ПЕРЕНАПРАВЛЕНИЕ ВВОДА/ВЫВОДА
#############################

# Записать вывод команды в файл
ls > files.txt

# Добавить вывод команды в файл
ls >> files.txt

# Передать вывод одной команды в другую
ls -l | grep ".txt"

# Использовать файл как ввод для команды
sort < data.txt

#############################
# 9. РАБОТА С ЛОГАМИ
#############################

# Показать последние строки файла
tail -f /var/log/syslog

# Показать первые 10 строк файла
head -n 10 file.txt

# Найти строку в файле
grep "error" logs.txt

# Найти файлы в системе
find / -name "*.log"
